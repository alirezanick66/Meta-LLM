/**
 * ‫انتخاب ۳ سوال رندوم از ۳ موضوع مختلف
 * @returns {Array} آرایه‌ای از ۳ سوال
 */
export const getRandomQuestions = () => {
	const topics = Object.keys(QUESTIONS)

	// ‫انتخاب ۳ موضوع رندوم بدون تکرار
	const shuffled = topics.sort(() => Math.random() - 0.5)
	const selectedTopics = shuffled.slice(0, 3)

	// ‫از هر موضوع یه سوال رندوم برمیداره
	return selectedTopics.map((topic) => {
		const questions = QUESTIONS[topic]
		return questions[Math.floor(Math.random() * questions.length)]
	})
}
